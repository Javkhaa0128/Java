package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello Everyone \n\tMy name is Cybertek");
        System.out.println("\tI am in Virginia\n");
        System.out.println("\n\n\tI \tlove \tprogramming");

        System.out.println("\\");   // this prints me one back slash
        System.out.println("/");    // this prints me one front slash
        System.out.println("\\\\"); // this prints me two back slash
        System.out.println("//");   // this prints me two front slash

        System.out.println("My name is \'Javkhlantugs\'");
        System.out.println("My name is 'Javkhlantugs'");
        System.out.println("My favorite TV-series; \"House\"");



    }
}
     //                   COMMON SCAPE SCEQUENCES


     //    \n      new line         - appends a new line
     //    \t      Horizantal tab   - appends paragraph space
     //    \\      Backslash        -
     //    \'      Single quote     - appends single quote
     //    \"      double quote     - "ONLY WAY"