package day22_NestedLoop;

public class Nested_Loop3 {
    public static void main(String[] args) {


        for(int i = 1; i <= 10; i++){

            for(int a = 1; a <= 5; a++){
                System.out.print("*");
            }
            System.out.println();

        }

        String star = "*";
        for(int i = 1; i <= 7; i++){

            System.out.println(star);
            star += "*";

        }
































    }
}
