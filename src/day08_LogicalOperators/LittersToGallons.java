package day08_LogicalOperators;

public class LittersToGallons {
    public static void main(String[] args) {
        /*  write a java program that converts litters to gallons
                1 gallon = 3.785  litters
                1  litters = 1/3.785 gallons
         */

        double litters = 200;
        double gallons = litters/3.785;
        System.out.println(litters + " litters "+"equals to "+(int)gallons+" gallons");





    }
}
